
class Feedback(object):

	def __init__(self):
		pass

	def setinfo(self, info):
		self.info = info

	def getinfo(self):
		return self.info

	def setdata(self, data):
		self.data = data

	def getdata(self):
		return self.data

	def setcommandtype(self, commandtype):
		self.commandtype = commandtype

	def getcommandtype(self):
		return self.commandtype